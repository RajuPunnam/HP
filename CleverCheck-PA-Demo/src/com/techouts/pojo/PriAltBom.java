package com.techouts.pojo;

import org.springframework.data.mongodb.core.mapping.Field;

public class PriAltBom {
@Field("PRI")
private String priPart;
@Field("ALT")
private String altPart;
@Field("ALT_DESC")
private String altDes;
@Field("PN_SOURCE")
private String pn_SOURCE;


public String getPn_SOURCE() {
	return pn_SOURCE;
}
public void setPn_SOURCE(String pn_SOURCE) {
	this.pn_SOURCE = pn_SOURCE;
}
public String getAltDes() {
	return altDes;
}
public void setAltDes(String altDes) {
	this.altDes = altDes;
}
public String getPriPart() {
	return priPart;
}
public void setPriPart(String priPart) {
	this.priPart = priPart;
}
public String getAltPart() {
	return altPart;
}
public void setAltPart(String altPart) {
	this.altPart = altPart;
}
@Override
public String toString() {
	return "PriAltBom [priPart=" + priPart + ", altPart=" + altPart + ", altDes=" + altDes + ", pn_SOURCE=" + pn_SOURCE
			+ "]";
}

}
